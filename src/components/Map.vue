// 商家分布图表
<template>
  <div class="com-container">
    <div class="com-chart" ref="map_ref"></div>
  </div>
</template>

<script>
import {getData} from '@api/data'
import { getProvinceMapInfo } from "@/utils/map_utils";
import axios from "axios";
export default {
  data() {
    return {
      chartInstance: null,
      allData: null,
      mapData:{}
    };
  },
  mounted() {
    this.initChart();
    this.getData();
    window.addEventListener("resize", this.screeAdapter);
    this.screeAdapter();
  },
  destroyed() {
    window.removeEventListener("resize", this.screeAdapter);
  },
  methods: {
    //初始化图表
    initChart() {
      this.chartInstance = this.$echarts.init(this.$refs.map_ref);
      //获取中国地图的矢量数据
      axios.get("http://localhost:8080/static/map/china.json").then((res) => {
        this.$echarts.registerMap("china", res.data);
        console.log(res.data);
      });
      const initOption = {
        title: {
          text: "▎ 商家分布",
          left: 20,
          top: 20,
        },
        geo: {
          type: "map",
          map: "china",
          top: "5%",
          bottom: "5%",
          itemStyle: {
            areaColor: "#2E428F",
            borderColor: "#333",
          },
        },
        legend: {
          left: "5%",
          bottom: "5%",
          orient: "vertical",
        },
      };
      this.chartInstance.setOption(initOption);
      this.chartInstance.on('click', async arg => {
        // arg.name 得到所点击的省份, 这个省份他是中文
        const provinceInfo = getProvinceMapInfo(arg.name)
        console.log(provinceInfo)
        // 需要获取这个省份的地图矢量数据
        // 判断当前所点击的这个省份的地图矢量数据在mapData中是否存在
        if (!this.mapData[provinceInfo.key]) {
          const ret = await axios.get('http://localhost:8999' + provinceInfo.path)
          this.mapData[provinceInfo.key] = ret.data
          this.$echarts.registerMap(provinceInfo.key, ret.data)
        }
        const changeOption = {
          geo: {
            map: provinceInfo.key
          }
        }
        this.chartInstance.setOption(changeOption)
      })
    },
    //获取数据
    getData() {
      getData("/map").then((res) => {
        this.allData = res.data;
        console.log(this.allData);
        this.updataChart();
      });
    },
    updataChart() {
      //处理图表需要的数据
      //图例的数据
      const legendArr = this.allData.map(item => {
        return item.name
      })
      const seriesArr = this.allData.map(item => {
        //return的这个对象就代表的是一个类别下的所有散点数据
        //如果想在地图中显示散点数据，我们需要给散点的图表增加一个配置， coordinateSystem:geo
        return {
          type: 'effectScatter',
          rippleEffect: {
            scale: 5,
            brushType:'stroke'
          },
          name: item.name,
          data: item.children,
          coordinateSystem:'geo'
        }
      })
      const dataOption = {
        legend: {
          data:legendArr
        },
        series: seriesArr
      };
      this.chartInstance.setOption(dataOption);
    },
    screeAdapter() {
      const titleFontSize = this.$refs.map_ref.offsetWidth / 100 *3.6
      const adapterOption = {
        title: {
          textStyle: {
            fontSize: titleFontSize
          }
        },
        legend:{
          itemWidth: titleFontSize / 2,
          itemHeight: titleFontSize / 2,
          itemGap: titleFontSize / 2,
          textStyle: {
            fontSize: titleFontSize / 2
          }
        }
      };
      this.chartInstance.setOption(adapterOption);
      this.chartInsance.resize();
    },
    //回到中国地图
    reverMap () {
      const revertOption = {
        geo: {
          map:'china'
        }
      }
      this.chartInsance.setOption(revertOption)
    }
  },
